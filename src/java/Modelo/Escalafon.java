package Modelo;

/**
 *
 * @author Daniel Nempeque
 */
public class Escalafon {
    private String id;
    private String tipo;

    public Escalafon(String id, String tipo) {
        this.id = id;
        this.tipo = tipo;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
}
